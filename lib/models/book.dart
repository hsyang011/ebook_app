class Book {
  var authors;
  var contents;
  var datetime;
  var isbn;
  var price;
  var publisher;
  var salePrice;
  var status;
  var thumbnail;
  var title;
  var url;

  Book({required this.authors, required this.contents, required this.datetime,
    required this.isbn, required this.price, required this.publisher, required this.salePrice,
    required this.status, required this.thumbnail, required this.title, required this.url});
}